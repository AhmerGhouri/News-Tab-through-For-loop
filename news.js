function getUniqueLists(responseText) {
    var resArr = [];

    responseText.filter(function (x, i) {

        if (resArr.indexOf(x.area) === -1) {

            resArr.push(x.area);

        }

    })
    return mergeDataAreaWise(resArr, responseText);
}

